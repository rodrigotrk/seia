package br.gov.ba.seia.middleware.sinaflor.model;
/**
 * Classe modelo do responsavel tecnico de entrada
 * @author 
 *
 */
public class ResponsavelTecnicoEntrada {
	
	private String art;
	private String pessoa;
	
	public ResponsavelTecnicoEntrada() {}

	public String getArt() {
		return art;
	}

	public void setArt(String art) {
		this.art = art;
	}

	public String getPessoa() {
		return pessoa;
	}

	public void setPessoa(String pessoa) {
		this.pessoa = pessoa;
	}
}
